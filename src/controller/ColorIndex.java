package controller;

/**
 * PALETTE color class
 */
public class ColorIndex {
    
    double index;
    double r;
    double g;
    double b;
    
    public ColorIndex(double index,double r, double g, double b){
        this.index = index;
        this.r = r;
        this.g = g;
        this.b = b;
    }

}
